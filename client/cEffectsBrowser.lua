function effectBrowser:KeyDown(args)
	if args.key == 39 then -- Navigate to next effect
		if self.eid == 467 then
			self.eid = 0
		else
			self.eid = self.eid + 1
		end
		self:fireEffect(self.eid)
	elseif args.key == 32 then -- Fire current effect
		self:fireEffect(self.eid)
	elseif args.key == 37 then -- Navigate to previous effect
		if self.eid == 0 then
			self.eid = 467
		else
			self.eid = self.eid - 1
		end
		self:fireEffect(self.eid)
	elseif args.key == 38 then -- Jump forward by 50 effects
		if self.eid + 50 > 467 then
			self.eid = 0
		else
			self.eid = self.eid + 50
		end
		self:fireEffect(self.eid)
	elseif args.key == 40 then -- Jump backward by 50 effects
		if self.eid - 50 < 0 then
			self.eid = 0
		else
			self.eid = self.eid - 50
		end
		self:fireEffect(self.eid)
	end
end
function effectBrowser:fireEffect(eid)
	if self.effect ~= nil then
		self.effect:Remove()
		self.effect = nil
	end
	self.effect = ClientEffect.Create(AssetLocation.Game, {
		effect_id = eid,
		
		position = Vector3(-6334.865234, 212.981903, -3570.047363),
		angle = Angle()
	})
end

function effectBrowser:Rend()
	local name = self.effectarray[self.eid + 1]
	local text = "Effect ID: " .. self.eid .. "   Name: " .. name
	local text2 = "Left/Right to change, Up/Down to change by 50, Space to re-fire"
	if string.find(text, "shake") then
		text = text .. " (Shakes camera when used)"
	end
	local pos = Vector2(Render.Width / 2 - (Render:GetTextWidth(text, TextSize.Large) / 2), Render.Height - 100 - Render:GetTextHeight(text2, TextSize.Large))
	local pos2 = Vector2(Render.Width / 2 - (Render:GetTextWidth(text2, TextSize.Large) / 2), Render.Height - 100)
	Render:DrawText(pos + Vector2(1,1), text, Color(0,0,0), TextSize.Large)
	Render:DrawText(pos2 + Vector2(1,1), text2, Color(0,0,0), TextSize.Large)
	Render:DrawText(pos, text, Color(255,255,255), TextSize.Large)
	Render:DrawText(pos2, text2, Color(255,255,255), TextSize.Large)
end

function effectBrowser:Unload()
	if IsValid(self.effect) then
		self.effect:Remove()
	end
end
